#!/bin/bash

# Перевіряємо, чи передано аргумент (коментар для коміту)
if [ $# -eq 0 ]; then
    echo "Помилка: Необхідно передати коментар для коміту"
    echo "Використання: ./pu.sh \"ваш коментар\""
    exit 1
fi

# Отримуємо коментар з першого аргументу
COMMIT_MESSAGE="$1"

echo "Додаємо всі файли до git..."
git add .

echo "Створюємо коміт з повідомленням: \"$COMMIT_MESSAGE\""
git commit -m "$COMMIT_MESSAGE"

echo "Відправляємо зміни на віддалений репозиторій..."
git push

echo "Готово! Всі зміни успішно відправлено."
